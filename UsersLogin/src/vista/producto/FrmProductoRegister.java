    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String name = txtName.getText();
        String mark = txtMark.getText();
        String category = txtCategory.getText();
        String price = txtPrice.getText();
        String stock = txtStock.getText();

        boolean tag = validatorCampos(name, mark, category, price, stock);

        if (tag) {
            factoryProduct1 = new FactoryProduct();

            Product product = factoryProduct1.product(name, mark, category,
                    Float.valueOf(price), Integer.valueOf(stock));
          
            if (!ConnexionProduct.create(product)) {
                System.out.println("La sentencia registrar no se completo "
                        + "correctamente.");
            } else {
                frmRecords.refreshTable();
                frmRecords.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private boolean validatorCampos(String name, String mark, String category,
            String price, String stock) {

        boolean tag = true;
        if (!ValidarCampos.isStringBlank(name)) {
            lblNameError.setIcon(
                    new ImageIcon(getClass().getResource("/iconos/error.png")));

            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios.", "CAMPO IMCOMPLETO",
                    JOptionPane.ERROR_MESSAGE);
            tag = false;
        } else if (!ValidarCampos.isStringBlank(mark)) {

            lblNameError.setIcon(null);
            lblMarkError.setIcon(
                    new ImageIcon(getClass().getResource("/iconos/error.png")));

            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios.", "CAMPO IMCOMPLETO",
                    JOptionPane.ERROR_MESSAGE);
            tag = false;
        } else if (!ValidarCampos.isStringBlank(category)) {

            lblMarkError.setIcon(null);
            lblCategoryError.setIcon(
                    new ImageIcon(getClass().getResource("/iconos/error.png")));

            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios.", "CAMPO IMCOMPLETO",
                    JOptionPane.ERROR_MESSAGE);
            tag = false;
        } else if (!ValidarCampos.isStringBlank(price)) {

            lblPriceError.setIcon(
                    new ImageIcon(getClass().getResource("/iconos/error.png")));

            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios.", "CAMPO IMCOMPLETO",
                    JOptionPane.ERROR_MESSAGE);
            tag = false;
        } else if (!ValidarCampos.isStringBlank(stock)) {
            lblCategoryError.setIcon(null);
            lblStockError.setIcon(
                    new ImageIcon(getClass().getResource("/iconos/error.png")));

            JOptionPane.showMessageDialog(this,
                    "Todos los campos son obligatorios.", "CAMPO IMCOMPLETO",
                    JOptionPane.ERROR_MESSAGE);
            tag = false;
        } else {
            lblStockError.setIcon(null);
        }
        return tag;
    }

